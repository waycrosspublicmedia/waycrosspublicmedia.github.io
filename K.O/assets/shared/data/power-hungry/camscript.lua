function onStepHit()
    if curStep == 1 then
        setProperty('cameraSpeed', 0.2)
    end


    if curStep >= 1439 and curStep <= 1963 then
        yy = 350
        setProperty('cameraSpeed', 3)
        ofs = 20
    elseif curStep >= 128 then
        setProperty('cameraSpeed', 1)
        yy = 550
        ofs = 10
    end
end



