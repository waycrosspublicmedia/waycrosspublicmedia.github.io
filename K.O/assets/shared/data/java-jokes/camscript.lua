function onEvent(tag)
    if tag == "followchars on" then
        followchars = true
    elseif tag == "followchars off" then
        followchars = false
    end
end



function onStepHit()

    

    if curStep <= 106 then
        xx = 700
        setProperty('cameraSpeed', 1)

    end
    if curStep >= 107 and curStep <= 910 then

        xx = 350
    end




end

previousCam = false
local kelVariable = false
function onSectionHit()
    if curStep > 115 and curStep < 1310 then
        kelVariable = mustHitSection
        if previousCam == kelVariable then
        else



            followchars = false
            triggerEvent('Add Camera Zoom')
            setProperty('cameraSpeed', 99)
            runTimer('cameraSpeed', 0.03)
            
            previousCam = kelVariable



            
        end
    end



    
end


function onTimerCompleted(n)
    if n == 'cameraSpeed' then
        setProperty('cameraSpeed', 1)
        followchars = true
    end
end

function onBeatHit()
    if curBeat >= 232 and curBeat <= 296 then
        
        xx = 700
        xx2 = 700
        yy = 500
        yy2 = 500
        triggerEvent('Werds Tween Zoom', 0.7, "0.01, backOut")

    else

    end
end

